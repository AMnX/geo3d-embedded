	public Pollutants(double[][] bornes, double[][] nox, double[][] hc,
			double[][] co) {

		boolean illegalParams = ((bornes == null) || (nox == null)
				|| (hc == null) || (co == null));
		illegalParams = illegalParams || (bornes.length != 2);

		if (illegalParams == false) {
			double[] regimes = bornes[0];
			double[] couples = bornes[1];

			int nbCouplesValues = couples.length - 1;
			int nbRegimeValues = regimes.length - 1;

			illegalParams = (nox.length < nbCouplesValues)
					|| (hc.length < nbCouplesValues)
					|| (co.length < nbCouplesValues)
					|| (nox.length < nbCouplesValues);
			illegalParams = illegalParams || (nox[0].length < nbRegimeValues)
					|| (hc[0].length < nbRegimeValues)
					|| (co[0].length < nbRegimeValues)
					|| (nox[0].length < nbRegimeValues);
		}

		if (illegalParams)
			throw new IllegalArgumentException("Matrices invalides");

		_bornes = bornes;
		this.nox = nox;
		this.hc = hc;
		this.co = co;
		this.enginespeed = _bornes[0];
		this.torque = _bornes[1];

	}

	public static double[][] loadDoubleArray(String filename) {
		int nbRecords = 0;
		int nbPoints = 0;

		double[][] array;

		try {
			File file = new File(filename);
			FileReader fileReader = new FileReader(file);

			BufferedReader buffReader = new BufferedReader(fileReader);
			String sLine = buffReader.readLine().trim();
			nbRecords = Integer.parseInt(sLine);
			sLine = buffReader.readLine().trim();
			nbPoints = Integer.parseInt(sLine);

			array = new double[nbRecords][nbPoints];

			for (int record = 0; record < nbRecords; record++) {
				sLine = buffReader.readLine().trim();
				int start = 0;
				int index = -1;
				String sValue;
				double value;

				for (int point = 0; point < nbPoints; point++) {
					index = sLine.indexOf(',', start);

					if (index >= 0) {
						sValue = sLine.substring(start, index);
					} else {
						sValue = sLine.substring(start);
					}

					value = Double.parseDouble(sValue);

					array[record][point] = value;

					start = index + 1;
				}
			}

			fileReader.close();

			return array;
		} catch (Exception ex) {
			return null;
		}
	}

	public static int dichotomieInterval(double element, double[] tableau) {
		int fin;
		int debut;
		int milieu;

		debut = 0;
		fin = tableau.length - 1;

		while (debut + 1 < fin) {
			milieu = (debut + fin) / 2;

			if (tableau[milieu] == element) {
				debut = milieu;
				break;
			} else if (tableau[milieu] < element) {
				debut = milieu;
			} else {
				fin = milieu;
			}
		}

		if (element > tableau[debut])
			return fin;
		else
			return debut;
	}

